		String[] wrong = {"()(())()()","(((()))())","((()(())))","()()(()())","(()(()))()","()(()()())","(()(()()))","()((()))()","()((()()))","(()()()())","(((()())))","()()()()()","((()())())","(())()()()","()(()())()","(()(())())","()(()(()))","(((())))()","(((())()))","()(((())))","(())(())()","(()((())))","(())((()))","()(())(())","()()(())()","()()((()))","((()))()()","((())()())","((((()))))","((()()()))","(()()())()","(()()(()))","()((())())","(())()(())","((()()))()","((())())()","()()()(())","((())(()))","(())(()())","(()())()()"};
